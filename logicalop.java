import java.util.*;
public class logicalop {
    public static void main(String args[]){
        Scanner scn = new Scanner(System.in);

        /*  LOGICAL AND - &&

        System.out.println( (3>2) && (5>2));
        System.out.println( (3<2) && (5>2));
        System.out.println( (3>2) && (5<2));
        System.out.println( (3<2) && (5<2));
        */

        /*  LOGICAL OR - ||

        System.out.println( (5>2) || (3>2));
        System.out.println( (5<2) || (3>2));
        System.out.println( (5>2) || (3<2));
        System.out.println( (5<2) || (3<2));
        */

        // LOGICAL NOT - !
        
        System.out.println( !(5>2) );
        System.out.println( !(3<2) );
    }
    
}
